class ConvertDataNamaLab {
  String convertData(String lab) {
    switch (lab) {
      case "1":
        return "A";
      case "2":
        return "B";
      case "3":
        return "C";
      case "4":
        return "D";
      case "5":
        return "E";
      case "6":
        return "G";
      case "7":
        return "H";
      case "8":
        return "I";
      case "9":
        return "J";
      case "10":
        return "K";
      case "11":
        return "L";
      case "12":
        return "M";
      case "13":
        return "N";
      default:
        return "";
    }
  }

}
